N = 10000;
v = randn(1,N);
B = [1];
A = [1 -1.2728 0.81];
d = filter(B,A,v);
bet = 0.01;
power = sum(d .^ 2) / N;
p = 2;
mu = bet * 2 / ((p+1)*power);
[_A,E] = lms_lp_analysis(d,mu,p);
t = [1:N] - 1;
subplot(2,1,1);
plot(t,_A(:,1),_A(:,2));
[_B,D] = lms_lp_synthesis(E,mu,p);
subplot(2,1,2);
plot(t,_B(:,1),_B(:,2));
